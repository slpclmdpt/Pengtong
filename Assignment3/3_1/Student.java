package Assignment3_1;

public class Student {
	private String name;
	private int id;

	// Constructor
	Student(String name, int id) {
		this.name = name;
		this.id = id;
	}

	// Accessor of name
	public String getName() {
		return name;
	}

	// Acessor of id
	public int getId() {
		return id;
	}

}
